package com.futuereh.dronefeeder.dto;

/**
 * Class DroneDto.
 *
 */
public class DroneDto {
  private String nome;
  private String modelo;

  /**
   * getNome.
   */
  public String getNome() {
    return nome;
  }

  /**
   * setNome.
   */
  public void setNome(String nome) {
    this.nome = nome;
  }

  /**
   * getModelo.
   */
  public String getModelo() {
    return modelo;
  }

  /**
   * setModelo.
   */
  public void setModelo(String modelo) {
    this.modelo = modelo;
  }

}
